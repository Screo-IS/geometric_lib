
def area(a):
    """функция принимает на вход число a, а потом при помощи формулы
    S = a * a находит и возвращает площадь квадрата"""
    return a * a


def perimeter(a):
    """функция принимает на вход число a, а потом при помощи формулы
        P = 4*a находит и возвращает площадь квадрата"""
    return 4 * a
